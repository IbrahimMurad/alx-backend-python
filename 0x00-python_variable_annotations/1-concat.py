#!/usr/bin/env python3
""" In this module, we define a function that concatenated two strings

"""


def concat(str1: str, str2: str) -> str:
    """concatenates str1 and str2 and returns the resulting string

    Args:
        str1 (str): the first string
        str2 (str): the second string to append to teh first

    Returns:
        str: the concatenated of the two passed strings
    """
    return str1 + str2
