#!/usr/bin/env python3
""" In this module, we define a function that adds to floats

"""


def add(a: float, b: float) -> float:
    """adds a and b and returns the result

    Args:
        a (float): the first number to add
        b (float): the second number to add

    Returns:
        float: the sum of the two passed floats
    """
    return a + b
